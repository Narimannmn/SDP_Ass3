package org.example;

public class LaptopControl {
    public void pressW(){
        System.out.println("Your character goes forward by keyboard");
    }
    public void pressA(){
        System.out.println("Your character goes left by keyboard");
    }
    public void pressS(){
        System.out.println("Your character goes back by keyboard");
    }
    public void pressD(){
        System.out.println("Your character goes right by keyboard");
    }
}
